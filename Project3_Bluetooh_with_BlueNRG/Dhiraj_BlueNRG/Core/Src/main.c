  // Init GATT
  aci_gatt_init();
  // Init GAP (Peripheral role)
  uint16_t service_handle, dev_name_char_handle, appearance_char_handle;
  aci_gap_init_IDB05A1(GAP_PERIPHERAL_ROLE_IDB05A1, 0, 0x07,
                 &service_handle, &dev_name_char_handle, &appearance_char_handle);
  aci_hal_set_tx_power_level(1, 4);   // high power, 0 dBm

  // Set device name
  const char name[] = "MyNode";
  aci_gatt_update_char_value(service_handle, dev_name_char_handle,
                               0, strlen(name), (uint8_t*)name);

   // Start advertising
   uint8_t adv_data[] = {
        2, AD_TYPE_FLAGS, FLAG_BIT_LE_GENERAL_DISCOVERABLE_MODE | FLAG_BIT_BR_EDR_NOT_SUPPORTED,
        8, AD_TYPE_COMPLETE_LOCAL_NAME, 'M','y','N','o','d','e'
    };

    aci_gap_set_discoverable(ADV_IND,
                             0x20, 0x30, // interval
                             PUBLIC_ADDR,
                             NO_WHITE_LIST_USE,
                             0, NULL,
                             0, NULL, 0, 0);

    aci_gap_update_adv_data(sizeof(adv_data), adv_data);


    // Add a custom service
    uint16_t hrServiceHandle, hrCharHandle;
    uint8_t hr_service_uuid[2] = { 0x00, 0xAA };  // 0x180D
    aci_gatt_add_serv(UUID_TYPE_16,
                      hr_service_uuid,
                      PRIMARY_SERVICE,
                      7,
                      &hrServiceHandle);

    // Add Heart Rate Measurement characteristic (notify)
    uint8_t hr_char_uuid[2] = { 0x00, 0xAA };     // 0x2A37
    aci_gatt_add_char(hrServiceHandle,
                      UUID_TYPE_16,
                      hr_char_uuid,
                      2,   // 2-byte value (e.g. bpm)
                      CHAR_PROP_NOTIFY,
                      ATTR_PERMISSION_NONE,
                      GATT_NOTIFY_ATTRIBUTE_WRITE,
                      16,
                      0,
                      &hrCharHandle);
  /* USER CODE END 2 */

  /* Infinite loop */
